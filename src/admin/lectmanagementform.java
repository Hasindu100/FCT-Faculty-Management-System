    private void btn_removeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_removeActionPerformed
        // TODO add your handling code here:
        try{
            if(txtid.getText().equals("")){
                JOptionPane.showMessageDialog(null, "Please Enter the lecturer's ID");
                return;
                // System.out.println("Lukshan");
            }
            int x = JOptionPane.showConfirmDialog(null,"Do you really want to delete this record");

            if(x==0){
                lecturer le = new lecturer();
                String up = "DELETE FROM lecturer WHERE lecture_id=?";
                ps = con.prepareStatement(up);
                le.setLecture_id(txtid.getText());
                ps.setString(1,le.getLecture_id());
                ps.executeUpdate();
                JOptionPane.showMessageDialog(null, "Deleted Successfully...");
                
            }
        }
        catch(Exception ex){
            JOptionPane.showMessageDialog(null, ex);
        }
        clear();
        showTable2Data();
    }//GEN-LAST:event_btn_removeActionPerformed

