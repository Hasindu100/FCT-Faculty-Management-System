    public void update(){
        try {
            if(txtid.getText().equals("")){
                JOptionPane.showMessageDialog(null, "Please Enter the lecturer's ID");
                return;
            }
           int x = JOptionPane.showConfirmDialog(null,"Do you really want to update this record");
        
        if(x==0){
            lecturer le = new lecturer(txtfname.getText(), txtlname.getText(), txtbd.getText(), (String) cmb_gender.getSelectedItem(), txttp.getText(), txtaddress.getText(), txtid.getText());
            
            String up = "Update lecturer SET fname='"+le.getFirst_Name()+"',lname='"+le.getLast_name()+"',birth_date='"+le.getBirth_date()+"',telephone_number='"+le.getMobile_no()+"',gender='"+le.getGender()+"',address='"+le.getAddress()+"' WHERE lecture_id=?";
            ps = con.prepareStatement(up);
            le.setLecture_id(txtid.getText());
            ps.setString(1,le.getLecture_id());
            ps.executeUpdate();
            JOptionPane.showMessageDialog(null, "updated Successfully...");
           
        }
        } catch(Exception ex){
            JOptionPane.showMessageDialog(null, ex);
        }
            clear();
    }
               
