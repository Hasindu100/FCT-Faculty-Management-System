    private void btn_searchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_searchActionPerformed
        try{
            if(txtid.getText().equals("")){
                JOptionPane.showMessageDialog(null, "Please Enter the lecturer ID");
                return;
            }
            lecturer l = new lecturer();
            String sql = "SELECT lecture_id,fname,lname,birth_date,telephone_number,gender,address FROM lecturer WHERE lecture_id=?";
            ps = con.prepareStatement(sql);
            l.setLecture_id(txtid.getText());
            ps.setString(1,l.getLecture_id());
            rs = ps.executeQuery();
            if(rs.next()){
                l.setLecture_id(rs.getString("lecture_id"));
                l.setFirst_Name(rs.getString("fname"));
                l.setLast_name(rs.getString("lname"));
                l.setBirth_date(rs.getString("birth_date"));
                l.setMobile_no(rs.getString("telephone_number"));
                l.setGender(rs.getString("gender"));
                l.setAddress(rs.getString("address"));

                txtid.setText(l.getLecture_id());
                txtfname.setText(l.getFirst_Name());
                txtlname.setText(l.getLast_name());
                txtbd.setText(l.getBirth_date());
                txttp.setText(l.getMobile_no());
                cmb_gender.setSelectedItem(l.getGender());
                txtaddress.setText(l.getAddress());
                showTable1Data();
            }
            else{
                JOptionPane.showMessageDialog(null,"Lecturer ID not found");
                clear();
                showTable1Data();
            }
        }
        catch(HeadlessException ex){
            JOptionPane.showMessageDialog(null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(lectmanagementform.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_btn_searchActionPerformed

