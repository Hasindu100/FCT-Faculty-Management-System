    private void btn_removeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_removeActionPerformed
        try{
            if(txtstid.getText().equals("")){
                JOptionPane.showMessageDialog(null, "Please Enter the student's ID");
                return;
            }
            ps2 = con.prepareStatement("select sid FROM student WHERE student.sid='"+txtstid.getText()+"' ");
            rs2 = ps2.executeQuery();
            
            
            if(rs2.next())
            {
            int x = JOptionPane.showConfirmDialog(null,"Do you really want to delete this record");
        
        if(x==0){
            student std = new student();
            String up = "DELETE FROM student WHERE sid=?";
            ps = con.prepareStatement(up);
            std.setStudent_id(txtstid.getText());
            ps.setString(1,std.getStudent_id());
            ps.executeUpdate();
            JOptionPane.showMessageDialog(null, "Deleted Successfully...");
            }
            }
            else{
                JOptionPane.showMessageDialog(null, "Student ID not found");
            }
        
        }
        catch(Exception ex){
            JOptionPane.showMessageDialog(null, ex);
        }
        showTableData();
        clear();
    }//GEN-LAST:event_btn_removeActionPerformed
    public void updateRecord() {
            try {
                if(txtstid.getText().equals("")){
                JOptionPane.showMessageDialog(null, "Please Enter the student's ID");
                return;
            }
            ps2 = con.prepareStatement("select sid FROM student WHERE student.sid='"+txtstid.getText()+"' ");
            rs2 = ps2.executeQuery();
            
            
            if(rs2.next())
            {
              int x = JOptionPane.showConfirmDialog(null,"Do you really want to update this record");
        
            if(x==0){
            student std = new student(txtstid.getText(), txtfname.getText(), txtlname.getText(), txtbd.getText(), (String) cmb_gender.getSelectedItem(), txttp.getText(), txtaddress.getText());
            
            String up = "Update student SET fname='"+std.getFirst_Name()+"',lname='"+std.getLast_name()+"',address='"+std.getAddress()+"',gender='"+std.getGender()+"',birth_date="
                    + "'"+std.getBirth_date()+"',telephone_number='"+std.getMobile_no()+"' WHERE student.sid=?";
            ps = con.prepareStatement(up);
            std.setStudent_id(txtstid.getText());
            ps.setString(1,std.getStudent_id());
            ps.executeUpdate();
            JOptionPane.showMessageDialog(null, "updated Successfully...");
            }
            }
            else{
                JOptionPane.showMessageDialog(null, "Student ID not found");
            }
    } catch(Exception ex){
            JOptionPane.showMessageDialog(null, ex);
        }

}
