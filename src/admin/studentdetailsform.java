    private void btn_searchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_searchActionPerformed
         try{
              if(txtstid.getText().equals("")){
                JOptionPane.showMessageDialog(null, "Please Enter the student's ID");
                return;
            }
            
          
            student std = new student();
            degree d = new degree();
            academic_year y = new academic_year();
            String sql = "SELECT student.sid,fname,lname,birth_date,gender,telephone_number,address,course.course_id,year FROM student,enroll,course,year,stdyear WHERE student.sid=enroll.sid AND course.course_id=enroll.course_id AND student.sid=stdyear.sid AND year.year_id=stdyear.year_id AND student.sid=?";
            ps = con.prepareStatement(sql);
            std.setStudent_id(txtstid.getText());
            ps.setString(1,std.getStudent_id());
            rs = ps.executeQuery();
            if(rs.next()){
                std.setFirst_Name(rs.getString("fname"));
                std.setLast_name(rs.getString("lname"));
                std.setBirth_date(rs.getString("birth_date"));
                std.setGender(rs.getString("gender"));
                std.setMobile_no(rs.getString("telephone_number"));
                std.setAddress(rs.getString("address"));
                d.setDegree_id(rs.getString("course_id"));
                y.setYear(rs.getString("year"));
            
                txtfname.setText(std.getFirst_Name());
                txtlname.setText(std.getLast_name());
                txtbd.setText(std.getBirth_date());
                cmb_gender.setSelectedItem(std.getGender());
                txttp.setText(std.getMobile_no());
                txtaddress.setText(std.getAddress());
                txtdegree.setText(d.getDegree_id());
                txtyear.setText(y.getYear());
            }
            else{
                JOptionPane.showMessageDialog(null,"Student ID not found");
                clear();
            }
        }
        catch(Exception ex){
            JOptionPane.showMessageDialog(null, ex);
        }
    }//GEN-LAST:event_btn_searchActionPerformed

